
public class Comments {

    public static void main(String[] args) {

        // Single-line comment
        System.out.println("This text is for the single line comment");

        /*
         * Multi-line comment:
         * The next line of code prints another message.
         * Multi-line comments are useful for longer explanations.
         */
        System.out.println("This text is an example of multi-line comments");

        /**
         * This is a document comment
         */
        System.out.println ("This is for documentation comment");

    }
}
